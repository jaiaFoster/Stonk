class TestBuildDoubleCalendarParents(unittest.TestCase):
    """_build_double_calendar_parents groups call+put calendars on same ticker+expirations."""

    def _make_struct(self, ticker, front, back, option_type, strike="210"):
        return {
            "ticker": ticker,
            "front_expiration": front,
            "back_expiration": back,
            "structure_type": option_type + "_calendar",
            "option_type": option_type,
            "current_debit": 2.50,
            "legs": [{"option_type": option_type, "strike": float(strike)}],
        }

    def test_call_and_put_paired_into_double_calendar(self):
        from app.services.open_options_position_reconciliation_service import _build_double_calendar_parents
        structures = [
            self._make_struct("SBUX", "2026-07-18", "2026-08-15", "call", "82"),
            self._make_struct("SBUX", "2026-07-18", "2026-08-15", "put", "80"),
        ]
        parents, unmatched = _build_double_calendar_parents(structures)
        self.assertEqual(len(parents), 1)
        self.assertEqual(len(unmatched), 0)
        parent = parents[0]
        self.assertEqual(parent["structure_type"], "double_calendar")
        self.assertEqual(parent["ticker"], "SBUX")
        self.assertIn("lower_strike", parent)
        self.assertIn("upper_strike", parent)
        self.assertIn("child_structure_ids", parent)
        self.assertEqual(len(parent["child_structure_ids"]), 2)

    def test_single_leg_stays_unmatched(self):
        from app.services.open_options_position_reconciliation_service import _build_double_calendar_parents
        structures = [
            self._make_struct("AAPL", "2026-07-18", "2026-08-15", "call"),
        ]
        parents, unmatched = _build_double_calendar_parents(structures)
        self.assertEqual(len(parents), 0)
        self.assertEqual(len(unmatched), 1)

    def test_different_tickers_not_paired(self):
        from app.services.open_options_position_reconciliation_service import _build_double_calendar_parents
        structures = [
            self._make_struct("AAPL", "2026-07-18", "2026-08-15", "call"),
            self._make_struct("MSFT", "2026-07-18", "2026-08-15", "put"),
        ]
        parents, unmatched = _build_double_calendar_parents(structures)
        self.assertEqual(len(parents), 0)
        self.assertEqual(len(unmatched), 2)

    def test_different_expirations_not_paired(self):
        from app.services.open_options_position_reconciliation_service import _build_double_calendar_parents
        structures = [
            self._make_struct("SBUX", "2026-07-18", "2026-08-15", "call"),
            self._make_struct("SBUX", "2026-07-25", "2026-08-22", "put"),
        ]
        parents, unmatched = _build_double_calendar_parents(structures)
        self.assertEqual(len(parents), 0)
        self.assertEqual(len(unmatched), 2)

    def test_total_debit_summed_from_children(self):
        from app.services.open_options_position_reconciliation_service import _build_double_calendar_parents
        s1 = self._make_struct("SBUX", "2026-07-18", "2026-08-15", "call")
        s1["current_debit"] = 2.00
        s2 = self._make_struct("SBUX", "2026-07-18", "2026-08-15", "put")
        s2["current_debit"] = 1.75
        parents, _ = _build_double_calendar_parents([s1, s2])
        self.assertAlmostEqual(parents[0]["current_total_debit"], 3.75, places=2)

    def test_four_legs_two_children_one_parent(self):
        """SBUX use-case: 4-leg double calendar → 2 child structures + 1 parent."""
        from app.services.open_options_position_reconciliation_service import _build_double_calendar_parents
        structures = [
            self._make_struct("SBUX", "2026-07-18", "2026-08-15", "call", "82"),
            self._make_struct("SBUX", "2026-07-18", "2026-08-15", "put", "80"),
        ]
        parents, unmatched = _build_double_calendar_parents(structures)
        self.assertEqual(len(parents), 1)
        self.assertEqual(len(unmatched), 0)
        self.assertIn("call_calendar", parents[0])
        self.assertIn("put_calendar", parents[0])
        self.assertIn("lifecycle_action", parents[0])
        self.assertIn("assignment_risk_summary", parents[0])


