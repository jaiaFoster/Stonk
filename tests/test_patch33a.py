class TestObservationKey(unittest.TestCase):
    """Deterministic observation_key construction."""

    def setUp(self):
        from app.db.strategy_opportunity_history import _observation_key
        self._key = _observation_key

    def test_calendar_with_front_and_back(self):
        row = {
            "ticker": "AAPL",
            "front_expiration": "2026-07-18",
            "back_expiration": "2026-08-15",
            "direction": "call",
            "strike": "200",
        }
        key = self._key("earnings_calendar", row)
        self.assertIn("AAPL", key)
        self.assertIn("2026-07-18", key)
        self.assertIn("2026-08-15", key)
        self.assertIn("call", key)

    def test_calendar_front_only_is_pre_window(self):
        row = {
            "ticker": "MSFT",
            "front_expiration": "2026-07-25",
            "earnings_date": "2026-07-28",
        }
        key = self._key("earnings_calendar", row)
        self.assertIn("pre_window", key)
        self.assertIn("MSFT", key)
        self.assertIn("2026-07-25", key)

    def test_stock_no_structure(self):
        row = {"ticker": "ELF"}
        key = self._key("forward_factor_calendar", row)
        self.assertEqual(key, "forward_factor_calendar:ELF")

    def test_earnings_calendar_with_date_only(self):
        row = {"ticker": "COST", "earnings_date": "2026-08-01"}
        key = self._key("earnings_calendar", row)
        self.assertIn("earnings_calendar", key)
        self.assertIn("COST", key)
        self.assertIn("2026-08-01", key)

    def test_key_is_lowercase_strategy_uppercase_ticker(self):
        row = {"ticker": "ibm"}
        key = self._key("Stock_Momentum", row)
        # strategy_id lowercased, ticker uppercased
        self.assertTrue(key.startswith("stock_momentum:IBM"))

    def test_same_inputs_produce_same_key(self):
        row = {"ticker": "TSLA", "front_expiration": "2026-07-18", "back_expiration": "2026-08-15"}
        k1 = self._key("earnings_calendar", row)
        k2 = self._key("earnings_calendar", row)
        self.assertEqual(k1, k2)


class TestWriteRun(unittest.TestCase):
    """write_run writes rows and returns accurate counts."""

    def _run(self, db_path, strategy_id, rows, run_id="run-001"):
        from app.db.strategy_opportunity_history import write_run
        return write_run(
            run_id=run_id,
            strategy_results={strategy_id: {"rows": rows}},
            run_date="2026-07-13",
            db_path=db_path,
        )

    def test_first_observation_counted(self):
        with tempfile.TemporaryDirectory() as d:
            db = os.path.join(d, "hist.db")
            result = self._run(db, "earnings_calendar", [
                {"ticker": "AAPL", "score": 80, "verdict": "PASS"},
            ])
        self.assertEqual(result["rows_written"], 1)
        self.assertEqual(result["first_observations"], 1)
        self.assertEqual(result["verdict_transitions"], 0)
        self.assertEqual(result["stage_transitions"], 0)

    def test_duplicate_run_ignored(self):
        with tempfile.TemporaryDirectory() as d:
            db = os.path.join(d, "hist.db")
            rows = [{"ticker": "AAPL", "score": 80, "verdict": "PASS"}]
            self._run(db, "earnings_calendar", rows, run_id="run-001")
            result = self._run(db, "earnings_calendar", rows, run_id="run-001")
